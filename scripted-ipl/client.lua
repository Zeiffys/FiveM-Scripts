	for k, v in pairs(scriptedipl) do
		if IsIplActive(v) then
			Citizen.Trace(" *scripted-ipl: "..v.." already loaded.\n")
		else
			RequestIpl(v)
			Citizen.Wait(0)
			if IsIplActive(v) then
				Citizen.Trace(" *scripted-ipl: "..v.." loaded.\n")
			else
				Citizen.Trace(" *scripted-ipl: "..v.." error while loading.\n")
			end
		end
	end
end)

